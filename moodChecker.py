mood = input("Hi, how are is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Come on, keep smiling!")

elif mood == "excited":
    print("That sounds great!")

elif mood == "relaxed":
    print("Relaxed is always good my friend!")

else:
    print("I don't recognize this mood.")